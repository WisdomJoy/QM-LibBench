# benchmark/run.py

from pathlib import Path
from loader import load_task, list_tasks
from prepare import prepare_workspace, cleanup_workspace
from patch import export_patch
import subprocess
import argparse
from typing import Optional

def fake_agent(task, repo_path: Path) -> Path:
    
    task_id = task.task_id
    target_file = task.metadata.get("target_file")
    
    if not target_file:
        raise ValueError(f"任务 {task.name} 缺少 target_file")
    
    print("=" * 80)
    print(f"TASK: {task_id}")
    print("=" * 80)
    print(f"\nRepo: {repo_path}")
    print(f"Target: {target_file}\n")
    
    target = repo_path / target_file
    target.parent.mkdir(parents=True, exist_ok=True)
    
    # 生成代码
    code = f"""// Generated by Agent for {task_id}
// Task: {task.name}
"""
    
    target.write_text(code, encoding="utf-8")
    print(f"✅ 生成: {target}")
    
    return target


def run_task(task_name: str, keep_workspace: bool = False) -> bool:
    """
    运行单个任务
    
    Args:
        task_name: 任务名称
        keep_workspace: 是否保留工作区
    
    Returns:
        bool: 是否成功
    """
    
    try:
        # 加载任务
        task = load_task(task_name)
        task_id = task.task_id
        
        print(f"\n{'=' * 80}")
        print(f"Running: {task_id}")
        print(f"{'=' * 80}\n")
        
        # 准备工作区
        repo_path, commit_id_start = prepare_workspace(task)
        
        # 运行 Agent
        generated_file = fake_agent(task, repo_path)
        print(f"Generated: {generated_file}\n")
        
        # 提交变更
        subprocess.run(["git", "add", "."], cwd=repo_path, check=True)
        
        subprocess.run(
            [
                "git",
                "-c", "user.name=agent",
                "-c", "user.email=agent@example.com",
                "commit",
                "-m",
                "agent_solution"
            ],
            cwd=repo_path,
            check=True
        )
        
        # 导出补丁
        patch_file = Path("../predictions") / f"{task_id}.patch"
        export_patch(repo_path, patch_file, commit_id_start)
        
        print(f"Saved: {patch_file}\n")
        
        # 清理工作区
        # if not keep_workspace:
        #     cleanup_workspace(repo_path.parent)  # 清理 workspace/{task_id}
        
        return True
        
    except Exception as e:
        print(f"❌ 任务 {task_name} 失败: {e}")
        return False
    
def main():
    """主函数"""
# 解析命令行参数
    parser = argparse.ArgumentParser(description="运行基准测试任务")
    parser.add_argument(
        "--task", "-t",
        help="指定运行的任务名称"
    )
    parser.add_argument(
        "--keep-workspace", "-k",
        action="store_true",
        help="保留工作区（不清理）"
    )
    parser.add_argument(
        "--list", "-l",
        action="store_true",
        help="列出所有任务"
    )
    parser.add_argument(
        "--repo", "-r",
        action="append",
        help="只检索指定仓库名下的任务；可重复使用，也支持逗号分隔"
    )
    
    args = parser.parse_args()
    
    # 确保结果目录存在
    Path("../predictions").mkdir(parents=True, exist_ok=True)
    
    # 列出任务
    if args.list:
        tasks = list_tasks(repo_names=args.repo)
        print(f"Found {len(tasks)} tasks:")
        for i, task in enumerate(tasks, 1):
            print(f"  {i}. {task}")
        return
    
    # 运行指定任务或所有任务
    tasks = list_tasks(repo_names=args.repo)
    
    if not tasks:
        print("⚠️ 没有找到任务")
        return
    
    if args.task:
        if args.task not in tasks:
            print(f"❌ 任务不存在: {args.task}")
            print(f"可用任务: {', '.join(tasks)}")
            return
        tasks = [args.task]
    
    print(f"Found tasks: {tasks}\n")
    
    # 运行任务
    success_count = 0
    for task_name in tasks:
        if run_task(task_name, args.keep_workspace):
            success_count += 1
    
    # 输出统计
    print(f"\n{'=' * 80}")
    print(f"完成: {success_count}/{len(tasks)} 个任务成功")
    print(f"{'=' * 80}")


if __name__ == "__main__":
    main()
